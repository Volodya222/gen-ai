"""
Синтетические персоны Отвечающего (техника курса: synthetic personas / homo silicus).

Каждая персона — это короткий демографический + ценностный профиль, которым мы
обусловливаем модель (silicon sampling в духе Argyle/Horton). Профили грубо
соответствуют сегментам, для которых в экспериментальной литературе известны
разные пороги отвержения в игре «Ультиматум».

Связь с реальными данными (для проверки гипотезы, блок 1 рубрики):
  В человеческих экспериментах (Güth et al. 1982; Oosterbeek et al. 2004, мета-
  анализ; Henrich et al. 2001, кросс-культурно) типичное предложение — 40–50%,
  а предложения ниже ~20–30% отвергаются примерно в половине случаев и чаще.
  Эти числа лежат в input/human_benchmark.json и служат эталоном для eval.

Каждая персона несёт ожидаемый «порог отвержения» (reject_below) — НЕ передаётся
модели, а используется только как ground-truth путь/ориентир в eval.
"""
from __future__ import annotations

from dataclasses import dataclass


@dataclass(frozen=True)
class Persona:
    id: str
    profile: str          # текст, которым обусловливаем модель
    reject_below: int     # ground-truth ориентир: ниже этого % обычно отвергает
    segment: str


PERSONAS: list[Persona] = [
    Persona(
        id="student_fair",
        segment="молодёжь, эгалитарные ценности",
        profile=(
            "Тебе 21 год, студент(ка) социологии из крупного города. Для тебя "
            "справедливость важнее денег: ты скорее накажешь жадного партнёра, "
            "потеряв небольшую сумму, чем стерпишь явно несправедливый делёж."
        ),
        reject_below=40,
    ),
    Persona(
        id="pragmatic_worker",
        segment="средний возраст, прагматик",
        profile=(
            "Тебе 38 лет, инженер(ка), считаешь деньги. Несправедливость "
            "раздражает, но небольшую сумму ты обычно берёшь — лучше что-то, "
            "чем ничего. Совсем мизерные подачки всё же отвергаешь из принципа."
        ),
        reject_below=25,
    ),
    Persona(
        id="poor_pragmatist",
        segment="низкий доход, высокая нужда",
        profile=(
            "Тебе 45 лет, доход низкий, каждая сотня рублей важна. Ты почти "
            "всегда принимаешь хоть что-то: гордость дешевле, чем еда. Отвергаешь "
            "лишь совсем оскорбительные предложения, близкие к нулю."
        ),
        reject_below=10,
    ),
    Persona(
        id="proud_principled",
        segment="принципиальный максималист",
        profile=(
            "Тебе 29 лет, предприниматель(ница) с обострённым чувством "
            "достоинства. Делёж, заметно отличающийся от равного, ты считаешь "
            "оскорблением и отвергаешь, даже теряя деньги."
        ),
        reject_below=45,
    ),
    Persona(
        id="elderly_traditional",
        segment="пожилой, традиционные нормы взаимности",
        profile=(
            "Тебе 67 лет, пенсионер(ка). Ценишь взаимность и норму «по-честному "
            "пополам». Умеренно несправедливое стерпишь, грубо несправедливое — нет."
        ),
        reject_below=30,
    ),
]


def get_persona(pid: str) -> Persona:
    for p in PERSONAS:
        if p.id == pid:
            return p
    raise KeyError(f"Нет персоны с id={pid!r}")
